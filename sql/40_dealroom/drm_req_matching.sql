USE DATABASE DEV_QUEBECTECH;

-- 0) Dealroom standardized
CREATE OR REPLACE TEMP TABLE TMP_DRM_STD AS
SELECT
  DEALROOM_ID,
  NAME AS DRM_NAME,
  TRADE_REGISTER_NUMBER AS DRM_TRADE_REGISTER_NUMBER,
  NULLIF(REGEXP_REPLACE(TRADE_REGISTER_NUMBER, '[^0-9]', ''), '') AS DRM_NEQ_DIGITS,
  CASE
    WHEN NULLIF(REGEXP_REPLACE(TRADE_REGISTER_NUMBER, '[^0-9]', ''), '') IS NULL THEN NULL
    WHEN LENGTH(NULLIF(REGEXP_REPLACE(TRADE_REGISTER_NUMBER, '[^0-9]', ''), '')) >= 10
      THEN LEFT(NULLIF(REGEXP_REPLACE(TRADE_REGISTER_NUMBER, '[^0-9]', ''), ''), 10)
    ELSE NULLIF(REGEXP_REPLACE(TRADE_REGISTER_NUMBER, '[^0-9]', ''), '')
  END AS DRM_NEQ_CLEAN,

  TRIM(
    REGEXP_REPLACE(
      REGEXP_REPLACE(
        REGEXP_REPLACE(
          UPPER(REPLACE(COALESCE(NAME,''), '&', ' AND ')),
          '[^A-Z0-9 ]', ' '
        ),
        '\\s+', ' '
      ),
      '\\b(INC|INCORPORATED|CORP|CORPORATION|LTD|LIMITED|LTÉE|LTEE|SAS|S\\.A\\.S|SENC|S\\.E\\.N\\.C|SENCRL|S\\.E\\.N\\.C\\.R\\.L|LP|LLP|CO|COMPANY|COMPAGNIE|GROUPE|GROUP)\\b',
      ''
    )
  ) AS DRM_NAME_STD,

  LEFT(
    TRIM(
      REGEXP_REPLACE(
        REGEXP_REPLACE(
          REGEXP_REPLACE(
            UPPER(REPLACE(COALESCE(NAME,''), '&', ' AND ')),
            '[^A-Z0-9 ]', ' '
          ),
          '\\s+', ' '
        ),
        '\\b(INC|INCORPORATED|CORP|CORPORATION|LTD|LIMITED|LTÉE|LTEE|SAS|S\\.A\\.S|SENC|S\\.E\\.N\\.C|SENCRL|S\\.E\\.N\\.C\\.R\\.L|LP|LLP|CO|COMPANY|COMPAGNIE|GROUPE|GROUP)\\b',
        ''
      )
    ),
    4
  ) AS DRM_LEFT4,

  SOUNDEX(
    TRIM(
      REGEXP_REPLACE(
        REGEXP_REPLACE(
          REGEXP_REPLACE(
            UPPER(REPLACE(COALESCE(NAME,''), '&', ' AND ')),
            '[^A-Z0-9 ]', ' '
          ),
          '\\s+', ' '
        ),
        '\\b(INC|INCORPORATED|CORP|CORPORATION|LTD|LIMITED|LTÉE|LTEE|SAS|S\\.A\\.S|SENC|S\\.E\\.N\\.C|SENCRL|S\\.E\\.N\\.C\\.R\\.L|LP|LLP|CO|COMPANY|COMPAGNIE|GROUPE|GROUP)\\b',
        ''
      )
    )
  ) AS DRM_SOUNDEX
FROM DEV_QUEBECTECH.BRONZE.DRM_COMPANY_BRONZE;

